# What's a dynamic url?
# url that gets generated automatically based on certain inputs

# What's url redirection?
# process in which users visits a particular url, they're navigated/guided to a next different url 

# How is URL Redirection is helpful?
# if a page is deleted, users need to be redirected to another url
# if maintenance of a web page is going on, users can be redirected 
# if website gets renamed, users can be redirected to appropriate URL 